    private void btnSubmitContent7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitContent7ActionPerformed
        String calc = lblCalcContent7.getText();
        int num1 = Integer.parseInt(txtNum1Content7.getText());
        int num2 = Integer.parseInt(txtNum2Content7.getText());
        int num3 = Integer.parseInt(txtNum3Content7.getText());
        int temp = 0;
        switch (calc) {
            case "+":
                temp = num1 + num2;
                break;
            case "-":
                temp = num1 - num2;
                break;
            case "*":
                temp = num1 * num2;
                break;
            case "/":
                temp = num1 / num2;
                break;
        }
        if (temp == num3) {
            lblResultContent7.setText("Yep!!!");
        } else {
            lblResultContent7.setText("Noob!!!");
        }
    }//GEN-LAST:event_btnSubmitContent7ActionPerformed
