    public void showTableResult() {
        modelStudent.setRowCount(0);
        for (Iterator<Student> obj = studentList.list_student.iterator(); obj.hasNext();) {
            Student s = obj.next();
            modelStudent.addRow(new Object[]{
                s.getId(), s.getName(), s.getBirthDay(), s.getAddress(), s.getClassroom(), s.getMath(), s.getPhysics(), s.getChemistry()
            });
        }
        btnEditContent10.setEnabled(false);
        btnDelContent10.setEnabled(false);
    }

